		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<Maticsoft.Model.Vehicle> DataTableToList(DataTable dt)
		{
			List<Maticsoft.Model.Vehicle> modelList = new List<Maticsoft.Model.Vehicle>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Maticsoft.Model.Vehicle model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = new Maticsoft.Model.Vehicle();					
													if(dt.Rows[n]["Id"].ToString()!="")
				{
					model.Id=int.Parse(dt.Rows[n]["Id"].ToString());
				}
																																				model.PlateNumber= dt.Rows[n]["PlateNumber"].ToString();
																																model.Driver= dt.Rows[n]["Driver"].ToString();
																																model.LinkTel= dt.Rows[n]["LinkTel"].ToString();
																																model.LinkAddress= dt.Rows[n]["LinkAddress"].ToString();
																																model.Remark= dt.Rows[n]["Remark"].ToString();
																						
				
					modelList.Add(model);
				}
			}
			return modelList;
		}

